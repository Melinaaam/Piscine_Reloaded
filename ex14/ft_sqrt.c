int	ft_sqrt(int nb)
{
	int	rac;

	rac = 1;
	if (nb <= 0)
	{
		return (0);
	}
	while (rac < nb / rac)
		rac++;
	if (rac * rac != nb)
		return (0);
	return (rac);
}
